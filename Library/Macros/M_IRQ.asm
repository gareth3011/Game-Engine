	.macro enterIRQ () {           						// IRQ Entry point
			pha
			txa
			pha
			tya
			pha                 						// (13)
	}

	.macro exitIRQ () {            						// IRQ Exit point
			lda #_ALL_BITS
			sta VIC._VICIRQ      						// Acknowledge the interrupt
			pla
			tay
			pla
			tax
			pla                 						// (19)
	}

	.macro enableRasterInterrupt () {					// Enable VIC raster interrupt as the interrupt source
			lda VIC._IRQMSK
			ora #_BIT_0									// Set bit 0 — raster interrupt enable
			sta VIC._IRQMSK
	}

	.macro disableTimerInterrupts () {					// Disable maskable IRQs
			lda #_R_BIT_7								// Disable timer interrupts which can be generated by the two CIA chips
			sta CIA._CIAICR
			sta CIA._CI2ICR
			lda CIA._CIAICR								// Ack any pending CIA interrupts
			lda CIA._CI2ICR
	}

	.macro clearRasterBit8 () {
			lda VIC._SCROLY
			and #_R_BIT_7		     					// Clear bit 8 of the raster
			sta VIC._SCROLY	
	}

